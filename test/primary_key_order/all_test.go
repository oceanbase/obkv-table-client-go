package primary_key_order

import (
	"os"
	"testing"

	"github.com/oceanbase/obkv-table-client-go/client"
	"github.com/oceanbase/obkv-table-client-go/test"
)

var cli client.Client

func setup() {
	cli = test.CreateClient()

	test.CreateDB()

	test.CreateTable(testPrimaryKeyTCreateStatement1)
	test.CreateTable(testPrimaryKeyTCreateStatement2)
	test.CreateTable(testPrimaryKeyTCreateStatement3)
}

func teardown() {
	cli.Close()

	test.DropTable(testPrimaryKeyTableName1)
	test.DropTable(testPrimaryKeyTableName2)
	test.DropTable(testPrimaryKeyTableName3)

	test.CloseDB()
}

func TestMain(m *testing.M) {
	setup()
	code := m.Run()
	teardown()
	os.Exit(code)
}
