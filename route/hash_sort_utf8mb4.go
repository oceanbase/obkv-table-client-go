// A continuation byte in UTF-8 is any byte where the top two bits are 10.
func isContinuationByte(c byte) bool {
	return (c & 0xC0) == 0x80
}

// Whether the buffer has enough bytes remaining.
func enoughBytesRemaining(ctx *obMbContext, n int) bool {
	return ctx.end >= ctx.start+n
}

/*
 * The following table shows how the Unicode code points map to UTF-8 multi-byte byte sequences,
 * and their equivalent binary values.
 * <p>
 * Unicode code points  Encoding  Binary value        UTF-8 Encoding
 * ------------------   --------------------------    ----------------------------------
 * U+000000-U+00007F    0xxxxxxx                      0xxxxxxx
 * U+000080-U+0007FF    00000yyy xxxxxxxx             110yyyxx 10xxxxxx
 * U+000800-U+00FFFF    yyyyyyyy xxxxxxxx             1110yyyy 10yyyyxx 10xxxxxx
 * U+010000-U+10FFFF    000zzzzz yyyyyyyy xxxxxxxx    11110zzz 10zzyyyy 10yyyyxx 10xxxxxx
 * <p>
 * The basic rules are as follows:
 * (1) If a byte starts with a 0 bit, it's a single byte value less than 128.
 * (2) If it starts with 11, it's the first byte of a multi-byte sequence
 * and the number of 1 bits at the start indicates how many bytes there are in total,
 * eg., 110xxxxx has two bytes, 1110xxxx has three and 11110xxx has four.
 * (3) If it starts with 10, it's a continuation byte.
 **/
func mbWcUtf8Mb4(ctx *obMbContext) int {
	if !enoughBytesRemaining(ctx, 1) {
		return csTooSmall
	}
	var b1, b2, b3, b4 byte // at most 4 bytes
	b1 = ctx.buf[ctx.start]
	if (b1 & 0x80) == 0 { // 1 byte: leading with 0
		ctx.pwc = int64(b1)
		return 1
	} else if (b1 & 0xE0) == 0xC0 { // 2 bytes : leading with 110
		if !enoughBytesRemaining(ctx, 2) {
			return csTooSmall2
		}
		b2 = ctx.buf[ctx.start+1]
		if !isContinuationByte(b2) {
			return csIlseq
		}
		ctx.pwc = int64(((b1 & 0x1F) << 6) | (b2 & 0x3F))
		if ctx.pwc < 0x0080 {
			return csIlseq
		}
		return 2
	} else if (b1 & 0xF0) == 0xE0 { // 3 bytes : leading with 1110
		if !enoughBytesRemaining(ctx, 3) {
			return csTooSmall3
		}
		b2 = ctx.buf[ctx.start+1]
		b3 = ctx.buf[ctx.start+2]

		if !(isContinuationByte(b2) && isContinuationByte(b3)) {
			return csIlseq
		}

		ctx.pwc = int64((uint32(b1&0x0F) << 12) | (uint32(b2&0x3F) << 6) | uint32(b3&0x3F))
		if ctx.pwc < 0x0800 || // overlong encoding, the range: U+000800-U+00FFFF.
			(ctx.pwc >= 0xD800 && ctx.pwc <= 0xDFFF) { // reserved for UTF-16 surrogate halves.
			return csIlseq
		}
		return 3
	} else if (b1 & 0xF8) == 0xF0 { // 4 bytes : leading with 11110
		if !enoughBytesRemaining(ctx, 4) {
			return csTooSmall4
		}

		b2 = ctx.buf[ctx.start+1]
		b3 = ctx.buf[ctx.start+2]
		b4 = ctx.buf[ctx.start+3]

		if !(isContinuationByte(b2) && isContinuationByte(b3) && isContinuationByte(b4)) {
			return csIlseq
		}
		ctx.pwc = int64((uint32(b1&0x07) << 18) |
			(uint32(b2&0x3F) << 12) |
			(uint32(b3&0x3F) << 6) |
			uint32(b4&0x3F))

		if ctx.pwc < 0x010000 || // overlong encoding, the range: U+010000-U+10FFFF.
			ctx.pwc > 0x10FFFF { // exceed the upper limit, the range: U+010000-U+10FFFF.
			return csIlseq
		}
		return 4
	}
	// error : utf8mb4 use at most 4 bytes.
	return csIlseq
}

func toSortUnicode(uniPlane *obUnicaseInfo, ctx *obMbContext, flags int) {
	if ctx.pwc <= int64(uniPlane.maxChar) {
		page := uniPlane.page[ctx.pwc>>8]
		if page != nil {
			if flags&csLowerSort != 0 {
				ctx.pwc = int64(page[ctx.pwc&0xFF].toLower)
			} else {
				ctx.pwc = int64(page[ctx.pwc&0xFF].sort)
			}
		}
	} else {
		ctx.pwc = csReplacementCharacter
	}
}

func hashAdd(ctx *obMbContext, ch int64) {
	ctx.n1 ^= (((ctx.n1 & 63) + int64(ctx.n2)) * ch) + (ctx.n1 << 8)
	ctx.n2 += 3
}

// HashSortUtf8Mb4 Ob hash sort utf8 mb4.
func hashSortUtf8Mb4(s []byte, n1 int64, n2 uint64, hashAlgo bool) int64 {
	ctx := &obMbContext{}
	ctx.buf = s
	ctx.n1 = n1
	ctx.n2 = n2
	ctx.start = 0
	ctx.end = len(s)
	ctx.hashBuf = make([]byte, csHashBufferLength)
	ctx.len = 0
	uniPlane := obUnicaseDefault
	for ctx.end > ctx.start && ctx.buf[ctx.end-1] == ' ' {
		ctx.end--
	}

	if hashAlgo {
		for res := mbWcUtf8Mb4(ctx); res > 0; res = mbWcUtf8Mb4(ctx) {
			toSortUnicode(&uniPlane, ctx, state)
			if ctx.len > (csHashBufferLength-2) ||
				(ctx.len == (csHashBufferLength-2) && ctx.pwc > 0xFFFF) {
				ctx.n1 = murmurHash64A(ctx.hashBuf, ctx.len, ctx.n1)
				ctx.len = 0
			}

			ctx.hashBuf[ctx.len] = byte(ctx.pwc & 0xFF)
			ctx.len += 1
			ctx.hashBuf[ctx.len] = byte((ctx.pwc >> 8) & 0xFF)
			ctx.len += 1
			if ctx.pwc > 0xFFFF {
				ctx.hashBuf[ctx.len] = byte((ctx.pwc >> 16) & 0xFF)
				ctx.len += 1
			}

			ctx.start += res
		}
		if ctx.len > 0 {
			ctx.n1 = murmurHash64A(ctx.hashBuf, ctx.len, ctx.n1)
		}
	} else {
		for res := mbWcUtf8Mb4(ctx); res > 0; res = mbWcUtf8Mb4(ctx) {
			toSortUnicode(&uniPlane, ctx, state)
			hashAdd(ctx, ctx.pwc&0xFF)
			hashAdd(ctx, ctx.pwc>>8&0xFF)
			if ctx.pwc > 0xFFFF {
				hashAdd(ctx, ctx.pwc>>16&0xFF)
			}
			ctx.start += res
		}
	}

	return ctx.n1
}

func toInt(b byte) int {
	return int(b & 0xFF)
}

func hashSortMbBin(s []byte, n1 int64, n2 uint64) int64 {
	// skip_trailing_space
	len := len(s)
	for ; len > 0 && s[len-1] == ' '; len-- {
		// do nothing
	}

	for i := 0; i < len; i++ {
		n1 ^= ((n1&63)+int64(n2))*int64(toInt(s[i])) + (n1 << 8)
		n2 += 3
	}

	return n1
}
